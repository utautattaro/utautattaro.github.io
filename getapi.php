function get_tiny_url($beforeurl)  {  
	$ch = curl_init();  
	$timeout = 5;  
	curl_setopt($ch,CURLOPT_URL,'http://tinyurl.com/api-create.php?url='.$beforeurl);  
	curl_setopt($ch,CURLOPT_RETURNTRANSFER,1);  
